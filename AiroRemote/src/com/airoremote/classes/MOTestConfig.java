package com.airoremote.classes;

import java.io.Serializable;

public class MOTestConfig implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5664986537590829266L;
	public String sPhoneNumber, sCallDuration, sPauseTime, sTestDuration;

	public MOTestConfig(String sPhoneNumber, String sCallDuration,
			String sPauseTime, String sTestDuration) {
		this.sPhoneNumber = sPhoneNumber;
		this.sCallDuration = sCallDuration;
		this.sPauseTime = sPauseTime;
		this.sTestDuration = sTestDuration;
	}

	@Override
	public String toString() {
		String toString = "Phone Number = " + sPhoneNumber
				+ ", Call Duration = " + sCallDuration + ", Pause Time = "
				+ sPauseTime + ", Test Duration = " + sTestDuration;
		return toString;
	}
}
