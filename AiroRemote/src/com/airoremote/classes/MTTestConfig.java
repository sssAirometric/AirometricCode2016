package com.airoremote.classes;

import java.io.Serializable;

public class MTTestConfig implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5664986537590829266L;
	public String sCallDuration, sTestDuration;

	public MTTestConfig(String sCallDuration, String sTestDuration) {
		this.sCallDuration = sCallDuration;
		this.sTestDuration = sTestDuration;
	}

	@Override
	public String toString() {
		String toString = "MTTestConfig[ Call Duration = " + sCallDuration
				+ ", Test Duration = " + sTestDuration + " ]";
		return toString;
	}
}
