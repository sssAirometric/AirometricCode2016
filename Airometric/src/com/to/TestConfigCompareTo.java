package com.to;

import java.io.Serializable;
import java.util.List;

/**
 * This Object Holds the information about each comparison added to the report
 * @author 100454
 *
 */
public class TestConfigCompareTo  implements Serializable{

	private static final long serialVersionUID = 2853860202092592781L;
	private Integer deviceId;
	private String deviceName;
	private String testConfigRouteName;
	private Integer testResultTypeId;
	private List<MarketInfo> makInfosList;
	private List <TestFileTo> testFileTosList;
	
	public Integer getDeviceId() {
		return deviceId;
	}
	public void setDeviceId(Integer deviceId) {
		this.deviceId = deviceId;
	}
	public String getDeviceName() {
		return deviceName;
	}
	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}
	public String getTestConfigRouteName() {
		return testConfigRouteName;
	}
	public void setTestConfigRouteName(String testConfigRouteName) {
		this.testConfigRouteName = testConfigRouteName;
	}
	public Integer getTestResultTypeId() {
		return testResultTypeId;
	}
	public void setTestResultTypeId(Integer testResultTypeId) {
		this.testResultTypeId = testResultTypeId;
	}
	public List<MarketInfo> getMakInfosList() {
		return makInfosList;
	}
	public void setMakInfosList(List<MarketInfo> makInfosList) {
		this.makInfosList = makInfosList;
	}
	public List<TestFileTo> getTestFileTosList() {
		return testFileTosList;
	}
	public void setTestFileTosList(List<TestFileTo> testFileTosList) {
		this.testFileTosList = testFileTosList;
	}
}
